/**
 * Enum representing log level prefixes used in plain text log formatting
 */
export enum LogPrefix
{
    /** Prefix for debug level logs */
    debug = "APP DEBUG:",

    /** Prefix for info level logs */
    info = "APP INFO:",

    /** Prefix for warning level logs */
    warning = "APP WARNING:",

    /** Prefix for error level logs */
    error = "APP ERROR:"
}